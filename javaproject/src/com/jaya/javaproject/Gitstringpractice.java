package com.jaya.javaproject;

public class Gitstringpractice {
	public static String stringTimes(String str,int n) {
		String name="";
		for(int i=0;i<n;i++) {
			name=name+str;
		}
		return str;
	}
	public static void main(String[] args) {
		System.err.println(stringTimes("bhanu",0));
	}
}
